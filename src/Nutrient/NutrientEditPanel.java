    private void btRecipeUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btRecipeUpdateMouseClicked
        if (checkRecipe()) {
            int id = Integer.parseInt(tfRecipeId.getText());
            String name = tfRecipeName.getText();
            String description = taRecipeDescription.getText();

            String tableName = "recipe";
            HashMap<String, Object> dietData = new HashMap<>();
            dietData.put("Name", name);
            dietData.put("Description", description);
            if (dao.Update(tableName, dietData, id)) {
                JOptionPane.showMessageDialog(null, "Recipe Update Successful");
            } else {
                JOptionPane.showMessageDialog(null, "Recipe Update Fault");
            }
        } else {
            JOptionPane.showMessageDialog(null, error);
        }
    }//GEN-LAST:event_btRecipeUpdateMouseClicked
    
    private boolean checkNutrient() {
        boolean result = true;
        error = "";
        if ("".equals(tfNutrientName.getText())) {
            error += "Name field can no empty\n";
        } else if (!ProjectLib.checkRegExp(tfNutrientName.getText(), "productName")) {
            error += "Name field invalid\n";
        }
        if ("".equals(taNutrientDescription.getText())) {
            error += "Description field can no empty\n";
        }
        if (!"".equals(error)) {
            result = false;
        }
        return result;
    }
    
