    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            HashMap<String, Object> employee = new HashMap<>();
            employee.put("Name", txtFullName.getText());
            SimpleDateFormat ft = new SimpleDateFormat("yyyy-MM-dd");
            String date = ft.format(dateBirth.getDate());
            employee.put("Birthday", date);
            employee.put("Phone", txtPhone.getText());
            byte gender = 1;
            if (rbtnFemale.isSelected()) {
                gender = (byte) 2;
            } else if (rbtnOther.isSelected()) {
                gender = (byte) 3;
            }
            employee.put("Gender", String.valueOf(gender));
            int role = cbxRole.getSelectedIndex() + 1;
            employee.put("Role", String.valueOf(role));
            employee.put("Email", txtEmail.getText());
            employee.put("Address", txtAddress.getText());
            employee.put("IdCard", txtCard.getText());
            employee.put("Salary", txtSalary.getText());
            employee.put("Note", txtNote.getText());
            employee.put("UserName", txtUserName.getText());
            employee.put("Password", txtPassword.getText().trim());
            DAOEmployee dao = new DAOEmployee();
            if (dao.update(employee, "employee", "ID = '" + rollNo + "';")) {
                JOptionPane.showMessageDialog(null, "Success!");
            }
            starter(rollNo);
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeEditPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

