    public void showNewPanel() {
        try {
            selectedId = -1;
            setEnable();
            pnMain.removeAll();
            FitnessNewPanel newPanel = new FitnessNewPanel();
            newPanel.setVisible(true);
            newPanel.setBounds(0, 0, pnMain.getWidth(), pnMain.getHeight());
            pnMain.add(newPanel);
        } catch (Exception e) {
            System.out.println("FitnessPanel :: btNewMouseClicked Error\n" + e);
        }
    }

