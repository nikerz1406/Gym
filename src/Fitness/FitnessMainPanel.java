    private void btEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btEditMouseClicked
        try {
            if(btEdit.isEnabled() == true) {
                pnMain.removeAll();
                FitnessEditPanel editPanel = new FitnessEditPanel();
                editPanel.setVisible(true);
                editPanel.setBounds(0, 0, pnMain.getWidth(), pnMain.getHeight());
                pnMain.add(editPanel);
            }
            else {
                JOptionPane.showMessageDialog(null, "Please select the row in table");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "FitnessPanel :: btEditMouseClicked Error\n" + e);
        }
    }//GEN-LAST:event_btEditMouseClicked

    private void btDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btDeleteMouseClicked
        if (btDelete.isEnabled() == true) {
            if (selectedArray != null) {
                String sqlTableName = "";
                switch (tableName) {
                    case "Course":
                        sqlTableName = "course";
                        break;
                    case "Package":
                        sqlTableName = "package";
                        break;
                    case "Exercise":
                        sqlTableName = "exercise";
                        break;
                    case "Book":
                        sqlTableName = "book";
                        break;
                }
                if (dao.Delete(sqlTableName, selectedArray)) {
                    JOptionPane.showMessageDialog(null, tableName + " Has Deleted");
                    showViewPanel();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select the row in table");
        }
    }//GEN-LAST:event_btDeleteMouseClicked

